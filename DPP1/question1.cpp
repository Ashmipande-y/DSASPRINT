// // // 1. Write a program to take two integers as 
// input and print their sum, difference, product, and
// //  quotient (integer division only).

#include <iostream>
using namespace std;

int main() {
    int a, b;
    cin >> a >> b;
    cout << a + b << endl;
    cout << a - b << endl;
    cout << a * b << endl;
    cout << a / b << endl; 
}
